set(handles.edit11,'String',0);
set(handles.edit12,'String',0);
set(handles.edit1,'String',0);
set(handles.edit2,'String',t);    
else
set(handles.edit3,'String',aap);
set(handles.edit4,'String',bjp);
set(handles.edit5,'String',bsp);
set(handles.edit6,'String',inc);
set(handles.edit7,'String',oth);
set(handles.edit8,'String',100*aap/(aap+bjp+bsp+inc+oth));
set(handles.edit9,'String',100*bjp/(aap+bjp+bsp+inc+oth));
set(handles.edit10,'String',100*bsp/(aap+bjp+bsp+inc+oth));
set(handles.edit11,'String',100*inc/(aap+bjp+bsp+inc+oth));
set(handles.edit12,'String',100*oth/(aap+bjp+bsp+inc+oth));
set(handles.edit1,'String',aap+bjp+bsp+inc+oth);
set(handles.edit2,'String',t);

end

